    private void buttonCheckoutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonCheckoutMouseClicked
        // TODO add your handling code here:
        if(userAuthLoginId == 0) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "Kamu Belum Login Nih, Login Dulu Yuk!");
            return;
        }
        if(menuSelected.length < 1) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "Kamu Pilih Menu Nih, Pilih Dulu Yuk!");
            return;
        }
        
        // Menghitung Menu yang Dipilih
        Map<Integer, Integer> countingMenuSelected = ArrayCounter.countElements(menuSelected);

        // Mendapatkan Posisi Tengah untuk Menempatkan Halaman Loading di Posisi Tengah
        Point centerPoint = Screen.getCenterPoint();

        // Mengalihkan ke Halaman Payment Gateway dan Mengatur Halaman Loading di Posisi Tengah
        Loading loadingPage = new Loading("PaymentGateway", false);
        loadingPage.setUserId(this.userAuthLoginId);
        loadingPage.setCheckoutList(countingMenuSelected);
        loadingPage.setLocation(centerPoint.x - loadingPage.getWidth() / 2, centerPoint.y - loadingPage.getHeight() / 2);
        loadingPage.setVisible(true);
        dispose();
    }//GEN-LAST:event_buttonCheckoutMouseClicked

