    private void menuButtonPrevMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menuButtonPrevMouseClicked
        // TODO add your handling code here:
        // Mengurangi 1 dari CurrentPages jika CurrentPages lebih dari 0
        if (currentPages > 0) {
            currentPages = (currentPages - 1);
        }

        // Memperbarui Menu dengan Halaman dan KategoriSaatIni
        menuId = MenuUpdater.updateMenu(
                componentSearch,
                currentMenuCategories,
                currentPages,
                menuButtonNext,
                menuButtonPrev
        );
    }//GEN-LAST:event_menuButtonPrevMouseClicked

    private void menuButtonNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menuButtonNextMouseClicked
        // TODO add your handling code here:
        // Menambahkan 1 pada HalamanSaatIni
        currentPages = currentPages + 1;

        // Memperbarui Menu dengan Halaman dan KategoriSaatIni
        menuId = MenuUpdater.updateMenu(
                componentSearch,
                currentMenuCategories,
                currentPages,
                menuButtonNext,
                menuButtonPrev
        );

        // Mendapatkan Semua ID Menu dari KategoriSaatIni
        int[] allMenuIds = db.getMenuIds(currentMenuCategories);
//
        // Mengelompokkan ID Menu dalam List<List<Integer>>
        List<List<Integer>> menuGrouping = MenuGrouping.groupArray(allMenuIds);

        // Mengurangi 1 dari HalamanSaatIni jika sudah mencapai HalamanTerakhir
        if (currentPages >= menuGrouping.size() - 1) {
            currentPages = currentPages - 1;
        }
    }//GEN-LAST:event_menuButtonNextMouseClicked

