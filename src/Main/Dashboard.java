    private void checkoutButtonNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_checkoutButtonNextMouseClicked
        // TODO add your handling code here:
        // Menambahkan 1 pada CurrentCheckoutPages
        currentCheckoutPages = (currentCheckoutPages + 1);

        // Memperbarui Menu Yang Dicheckout dengan 0, CurrentCheckoutPages, DB, ComponentSearch, CheckoutButtonPrev, dan CheckoutButtonNext
        menuSelected = updateCheckout(
                menuSelected,
                0,
                currentCheckoutPages,
                componentSearch,
                checkoutButtonPrev,
                checkoutButtonNext
        );

        // Mengelompokkan Menu Yang Dicheckout dalam List<Map<Integer, Integer>> Menggunakan CheckoutGrouping
        List<Map<Integer, Integer>> checkoutSelectedMenuGrouping = new CheckoutGrouping().Group(ArrayCounter.countElements(menuSelected));

        // Mengurangi 1 dari CurrentCheckoutPages jika melebihi ukuran (checkoutSelectedMenuGrouping.size() - 1)
        if (currentCheckoutPages > (checkoutSelectedMenuGrouping.size() - 1)) {
            currentCheckoutPages = (currentCheckoutPages - 1);
        }
    }//GEN-LAST:event_checkoutButtonNextMouseClicked

